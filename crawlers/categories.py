def guess_category(title, extra_keywords=None):
    """根据竞赛标题和可选的额外关键词猜测分类

    Args:
        title: 竞赛标题
        extra_keywords: 额外的关键词列表（如来源网站的分类标签）

    Returns:
        标准化分类名称
    """
    # 先检查额外关键词
    if extra_keywords:
        for kw in extra_keywords:
            for keyword, category in MASTER_CATEGORY_MAP.items():
                if keyword in kw:
                    return category

    # 再检查标题
    for keyword, category in MASTER_CATEGORY_MAP.items():
        if keyword in title:
            return category

    return "其他"
