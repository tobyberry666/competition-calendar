def _guess_difficulty(title):
    if "ICPC" in title or "CCPC" in title or "程序设计" in title or "算法" in title:
        return "Advanced"
    if "挑战杯" in title or "互联网+" in title or "创业" in title:
        return "Intermediate"
    return "Beginner"


def get_whitelist_competitions():
    """返回教育部 84 项白名单的结构化竞赛列表（权威锚点）"""
    now = datetime.now().strftime("%Y-%m-%d")
    result = []
    for rank, name, organizer, url in _WHITELIST:
        comp = {
            "id": make_id(name),
            "name": name,
            "category": guess_category(name),
            "subcategory": [],
            "organizer": organizer,
            "location": {"province": "", "city": "", "display": "全国"},
            "timeline": {
                "registrationStart": None,
                "registrationDeadline": None,
                "submissionDeadline": None,
                "competitionDate": None,
                "resultDate": None,
            },
            "description": f"教育部《2023 全国普通高校大学生竞赛分析报告》认可赛事（白名单第 {rank} 项）。",
            "officialUrl": url,
            "source": "教育部白名单(84项)",
            "sourceVerified": True,
            "whitelistRank": rank,
            "confidence": "high",
            "lastUpdated": now,
            "difficulty": _guess_difficulty(name),
            "prize": "",
            "region": "national",
            "status": "待更新",
        }
        result.append(comp)
    print(f"[白名单] 加载 {len(result)} 项教育部认可赛事（含官网 {sum(1 for c in result if c['officialUrl'])} 条）")
    return result


